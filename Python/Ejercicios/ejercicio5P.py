print("Ingrese el primero numero: ")
num1 = int(input())
print("Ingrese el segundo numero: ")
num2 = int(input())
print("Ingrese el tercer numero: ")
num3 = int(input())
if num1 > 0:
    num4 = num1 * num2 * num3
    print("El producto es: " , num4)
else:
    num5 = num1 + num2 + num3
    print("La suma es: " , num5)